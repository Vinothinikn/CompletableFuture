package com.completablefuture.dto;

public class EmployeeJsonDTO {

	String employeeId;
	String firstName;
	String lastName;
	String email;
	String gender;
	String newJoiner;
	String learningPending;
	int salary;
	int rating;
	
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getNewJoiner() {
		return newJoiner;
	}
	public void setNewJoiner(String newJoiner) {
		this.newJoiner = newJoiner;
	}
	public String getLearningPending() {
		return learningPending;
	}
	public void setLearningPending(String learningPending) {
		this.learningPending = learningPending;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public int getRating() {
		return rating;
	}
	public void setRating(int rating) {
		this.rating = rating;
	}
	
	
}
